// Fonctions utilitaires pour le SEO
export const getMetaTitle = (page: { title: string }) => {
  return `${page.title} | Offres vérifiées par IA`;
};

export const getMetaDescription = (page: { description: string }) => {
  return `${page.description} Trouvez votre prochaine opportunité sur FreeMatch.`;
};

export const getCanonicalUrl = (page: { path: string }) => {
  return `${WEBSITE_URL}${page.path}`;
};

export const getSeoDataFromSlug = ({ type, slug }: { type: string; slug?: string | string[] }) => {
  const id: string = `${type}-${Array.isArray(slug) ? slug.join("-") : slug}`;
  const seo = SEO_DATA.find((route) => route.id === id);

  if (!seo) return null;

  return seo;
};
